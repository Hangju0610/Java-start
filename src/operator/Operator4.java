package operator;

public class Operator4 {
    public static void main(String[] args) {
        // 연산자를 알아볼 수 있게 괄호를 넣어주자.
        // 명시적으로 작성하기 기본
        int sum3 = 2 * 2 + 3 * 3; // (2*2) + (3*3)
        int sum4 = (2 * 2) + (3 * 3);
        System.out.println("sum3 = "+ sum3);
        System.out.println("sum4 = "+ sum4);
    }
}
